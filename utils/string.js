/**
 *
 * @param {string} string
 */
module.exports.underline = function underline(string) {
    return string
        .split('')
        .map(x => `\u0332${x}`)
        .join('');
};

module.exports.overline = function overline(string) {
    return string
        .split('')
        .map(x => `\u0305${x}`)
        .join('');
};

