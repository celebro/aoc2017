/**
 * @param {string} input
 */
function run(input) {
    let part1 = undefined;
    let part2 = undefined;
    const lines = input.split('\n').filter((line) => line.length);

    const grid = new Grid();
    let cycle = 0;
    let x = 1;
    let signal = 0;
    lines.forEach((line) => {
        doCycle();

        const [cmd, param] = line.split(' ');
        if (cmd === 'addx') {
            doCycle();
            x += +param;
        }
    });

    function doCycle() {
        cycle++;
        const row = Math.floor((cycle - 1) / 40);
        const col = (cycle - 1) % 40;
        let value = '░';
        if (Math.abs(x - col) <= 1) {
            value = '█';
        }
        grid.set(col, row, value);

        if ((cycle - 20) % 40 === 0) {
            signal += cycle * x;
        }
    }
    part1 = signal;
    grid.print();

    return [part1, part2];
}

for (const testInput of testInputs) {
    if (testInput) {
        const testResult = run(testInput);
        console.log('test: ', testResult.join(' / '));
    }
}

const result = run(input);
